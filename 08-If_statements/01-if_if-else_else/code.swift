// If statement
var number = 1

if number > 5 {
    print("Yes")
} else { 
    print("Not greater than five")
}

// [Else if = only runs when the first condition is false]
// It works like a backup condition
var num2 = 10

if num2 < 4 {
    print("Less than four")
} else if num2 < 100 {
    print("But less than one hundred")
}