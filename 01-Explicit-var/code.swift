// Explicit variables

// String
var myName: String = "Harold Ramos"
print("Full name =", myName)

// Integers
var myAge: Int = 34
print("I am \(myAge) years old")

// Double
var bankAccount: Double = 1000000.56
print("I have in my bank account \(bankAccount) dollars.")

// Float
var myChange: Float = 34.56
print("My change was of \(myChange) dollars")

// Bool
var married: Bool = true
print(married)

// Character
var A: Character = "A"
print("Test result =", A)

// Multiple variables in a single line
var n1: Int = 10, n2 = 20, n3 = 30
print(n1, n2, n3)