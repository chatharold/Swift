// Creating an Int alias
typealias integer = Int

var number: integer = 20
print("Integer =", number)
print("Type =", type(of:number))


// Creating a Double alias
typealias decimal = Double

var dec: decimal = 3.014159
print("Double =", dec)
print("Type =", type(of:dec))


// Creating a String alias
typealias letters = String

var name: letters = "Harold"
print("Result =", name)
print("Type =", type(of:name))