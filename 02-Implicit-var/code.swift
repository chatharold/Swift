// Implicit

// String
var myName = "Harold Ramos"
print("My name is =", myName)

// Integer
var myAge = 34
print("I am \(myAge) years old.")

// Double
var bankAccount = 120000.56
print("My bank account = \(bankAccount)")

// Float
var myChange = 12.45
print("My change = \(myChange)")

// Bool
var married = true
print(married)

// Character
var A = "A"
print("Test result =", A)

// Multiple variables in a single line
var n1 = 10, n2 = 20, n3 = 30
print(n1, n2, n3)