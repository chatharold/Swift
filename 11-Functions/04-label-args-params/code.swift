// arg parameter: dataType

// Single parameter
func showNumber(argLabel parameter: Int) {
    print("Number =", parameter)
}

showNumber(argLabel: 20)


// Multiple parameters
func addNumbers(argLabel1 param1:Int, argLabel2 param2:Int) {
    print("Result =", param1 + param2)
}

addNumbers(argLabel1: 20, argLabel2: 40)

